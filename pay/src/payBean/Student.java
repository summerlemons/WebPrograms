package payBean;

public class Student {
	private int id;
	private String yb_userid;
	private String yb_username;
	private String yb_collegename;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getYb_userid() {
		return yb_userid;
	}
	public void setYb_userid(String yb_userid) {
		this.yb_userid = yb_userid;
	}
	public String getYb_username() {
		return yb_username;
	}
	public void setYb_username(String yb_username) {
		this.yb_username = yb_username;
	}
	public String getYb_collegename() {
		return yb_collegename;
	}
	public void setYb_collegename(String yb_collegename) {
		this.yb_collegename = yb_collegename;
	}
	
	
}
